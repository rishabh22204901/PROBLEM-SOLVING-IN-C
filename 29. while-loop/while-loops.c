// WAP to print 1 - 100 by using while loops ==================

// =============================================================================

// koi bhi condition ban rhi hhai aur wo toot ti jaa rhi hai
#include <stdio.h>
int main(){
    int i = 1;
while (i <= 100){  // andar condition likhi hoti hai hai aur jab hame jada chize pata nhi hoti hai tab ham while loops ka use karte hai 
    printf("%d\n", i);
    i++;
}
}